document.getElementById('fetchButton').addEventListener('click', function() {
    alert("Fetching random book data from backend!")
    fetch('http://localhost:3000/books')//query the backend server (app.js)
        .then(response => response.json())
        .then(data => {
            const randomBook = data[Math.floor(Math.random() * data.length)];

            document.getElementById('bookId').textContent = randomBook.id;
            document.getElementById('bookTitle').textContent = randomBook.title;
            document.getElementById('bookAuthor').textContent = randomBook.author;
        })

        .catch(error => console.error('Error fetching data: ', error));
});

document.getElementById('createButton').addEventListener('click', function() {
    alert("Added new book to DB!");
    fetch('http://localhost:3000/books', {
        method: "POST",
        headers: {
            "Content-Type": "application/json",
        },
        body: JSON.stringify({
            // id is omitted or should be generated by backend
            title: document.getElementById('newBookTitle').value,
            author: document.getElementById('newBookAuthor').value
        })
    }) // query the backend server (app.js)
    .then(response => response.json())
    .then(data => {
        console.log('Book added:', data);
    })
    .catch(error => console.error('Error adding book:', error));
});
